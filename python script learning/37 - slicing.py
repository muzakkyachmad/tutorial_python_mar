#slicing is process of getting a part of a list or other iterable

friends = ["rolf", "charlie", "anna", "bob", "jen"]

print(friends[2:4]) #[2:4] means to print only number 2 and 4 in the list

print(friends[1:]) #blank colon means skipping

print(friends[:3]) #structure = [start point : ending point]

print(friends[:])

print(friends[-3:]) #to start the calculation from behind

print(friends[-3:-1])
